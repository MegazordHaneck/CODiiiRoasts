function normalize(s) {
  if (typeof s !== 'string') return '';
  return s.toLowerCase().replace(/\s+/g, ' ').trim();
}

const ROUTING_TEXT_MAX_CHARS = 120000;

function buildSignalIndex(signalMap) {
  const indexed = {};
  for (const [key, signals] of Object.entries(signalMap)) {
    indexed[key] = signals
      .map((sig) => {
        const normalized = normalize(sig);
        if (!normalized) return null;
        return {
          raw: sig,
          normalized,
          weight: normalized.length >= 18 ? 2 : 1
        };
      })
      .filter(Boolean);
  }
  return indexed;
}

function getDocTextSample(doc) {
  const raw = [
    doc?.title,
    doc?.name,
    doc?.fileName,
    doc?.category,
    doc?.text,
    doc?.content,
    doc?.rawText,
    doc?.extractedText,
    doc?.ocrText
  ]
    .filter(Boolean)
    .join(' ');
  return normalize(raw.slice(0, ROUTING_TEXT_MAX_CHARS));
}

function scoreSignalMap(text, signalIndex) {
  const ranked = [];
  for (const [key, signals] of Object.entries(signalIndex)) {
    let score = 0;
    const matchedSignals = [];
    for (const sig of signals) {
      if (text.includes(sig.normalized)) {
        score += sig.weight;
        matchedSignals.push(sig.raw);
      }
    }
    if (score > 0) ranked.push({ key, score, matchedSignals });
  }

  ranked.sort((a, b) => b.score - a.score);
  const topScore = ranked[0]?.score || 0;
  const secondScore = ranked[1]?.score || 0;
  const confidence = topScore <= 0 ? 0 : Number((topScore / (topScore + secondScore + 1)).toFixed(3));

  return {
    top: ranked[0] || null,
    ranked,
    confidence
  };
}

